//  ************** RECORRIDO A TODOS LOS ENLACES SIGNOS PARA APLICAR LA ATENCIÓN AL QUE SE HAGA CLICK Y VOLCAR SUS CONTENIDOS ***************


signosEnlaces.forEach(function (signoEnlace) {
    signoEnlace.addEventListener("click", function(){

        let signo=this.id;

        while (volcado.firstChild) {
            volcado.removeChild(volcado.firstChild);
        }

        let img=document.createElement("img");
        img.src=signos[signo][imagen];
        imagen.appendChild(img);
        
        titulo.textContent=signos[signo][titulo];
        anos.textContent=signos[signo][anos];
        subtitulo.textContentL=signos[signo][subtitulo];
        caracteristicas.textContent=signos[signo][caracteristicas];
    });
});


